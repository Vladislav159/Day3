const cartProducts = [];

const addToCart = (id) => {
    // e.prevent.Default
    const product = shoppingData.products.find(el => el.id === id);
    const cartIndex = cartProducts.findIndex(el => el.id === id);

    discountPrice = (product.price - (product.price / 100 * product.discountPercentage)).toFixed(2);

    const sum = cartProducts.reduce((prev, current) => {
        return prev + current.price * current.qty;
      }, 0);

    if (cartIndex > -1) {
        //Atnaujinimas
        cartProducts[cartIndex].qty++;
    } else {
        //Pridejimas 
        cartProducts.push({
            id: product.id,
            title: product.title,
            thumbnail: product.thumbnail,
            description: product.description,
            price: +product.price,
            discountPercentage: product.discountPercentage,
            qty: 1
        });

    }

    const display = cartProducts.map(value => {
        return `   
                        <div class="rowTwo "  >
                            <div class="col-auto image">
                                <img src="${value.thumbnail}">
                            </div>
                          
                            <div class="col inform">
                                <h4>${value.title}</h4>
                                <div class="description pt-5">${value.description}</div>
                            </div>

                            <div class="butt"  >
                                <span onclick="remToQuantity(${value.id})">-</span>
                                <input type="number" class="form-control numero" style="width: 70px;" value="${value.qty}">
                                <span onclick="addToQuantity(${value.id})">+</span>
                            </div>

                            <div class="price mb-3 prize " >
                                <span class="original-price" >&dollar;${discountPrice}</span>
                            </div>
                        </div> 
                `;
    });

   

    let html = ` 
            <div class="d-flex">
                <div style="    width: 1000px;">
                
                    <div class="d-flex shopi">
                        <h1>Shopping cart</h1>
                        <p>${display.length}item(-s)</p>
                    </div> 
        
                    ${display.join('')}

                    <div class="backto">
                        <button type="button" class="btn btn-light" onclick="backtoProducts(event)">Back to Shop</button>
                    </div>
                </div>  
                <div class="Summary">
                    <h2>Summary</h2>
                    <div class="sumprice">
                        <p>${display.length} items(-s)</p>
                        <p class="total">$${sum}<p>
                    </div>
                    <div>
                        <h2>SHIPPING</h2>
                        <input type='choose'>
                        <h2>GIVE CODE</h2>
                        <input type="text"> 
                        
                    </div>
                    <div class="register">
                        <p class="total">TOTAL PRICE   $${sum}</p>
                        <button type="button" class="btn btn-dark">REGISTER</button>
                    </div>

                </div>
            </div>
            `;


    console.log(cartProducts);
    
    document.querySelector('.cart').innerHTML = html;
    document.querySelector('.products').style.display = `none`;
    document.querySelector(`.cart`).style.display = `block`;
}

